"""Los operadores de comparación se utilizan para comparar dos valores y devolver un valor booleano (True o False).
Los operadores de comparación más comunes son:"""
# Operadores de comparación
# Ejemplos de operadores de comparación
print("Operadores de comparacion con numeros")
print("5 == 5:", 5 == 5)      # True, porque 5 es igual a 5
print("3 != 4:", 3 != 4)      # True, porque 3 no es igual a 4
print("7 > 2:", 7 > 2)        # True, porque 7 es mayor que 2
print("4 < 1:", 4 < 1)        # False, porque 4 no es menor que 1
print("6 >= 6:", 6 >= 6)      # True, porque 6 es mayor o igual a 6
print("2 <= 3:", 2 <= 3)      # True, porque 2 es menor o igual a 3

# Comparación de cadenas
print("\nOperadores de comparacion con cadenas")
print('"apple" == "apple":', "apple" == "apple")  # True, porque las cadenas son iguales
print('"Hola" == "hola":', "Hola" == "hola")      # False, porque las cadenas son diferentes (distingue mayúsculas y minúsculas)
print('"banana" != "apple":', "banana" != "apple")  # True, porque las cadenas son diferentes
print('"cherry" > "banana":', "cherry" > "banana")  # True, porque "cherry" es mayor que "banana" en orden alfabético
print('"date" < "fig":', "date" < "fig")            # True, porque "date" es menor que "fig" en orden alfabético
print('"grape" >= "grape":', "grape" >= "grape")    # True, porque las cadenas son iguales
print('"kiwi" <= "lemon":', "kiwi" <= "lemon")      # True, porque "kiwi" es menor o igual que "lemon" en orden alfabético
print("Monterrey > Guadalajara:", "Monterrey" > "Guadalajara")  # True, porque "Monterrey" es mayor que "Guadalajara" en orden alfabético
